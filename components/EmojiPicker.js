export default function EmojiPicker({ onSelect, isOpen, onClose }) {
  const [activeCategory, setActiveCategory] = useState(Object.keys(EMOJI_CATEGORIES)[0])
  const [searchQuery, setSearchQuery] = useState('')
  const pickerRef = useRef(null)

  useEffect(() => {
    const handleClickOutside = (event) => {
      if (pickerRef.current && !pickerRef.current.contains(event.target)) {
        onClose()
      }
    }

    if (isOpen) {
      document.addEventListener('mousedown', handleClickOutside)
      return () => document.removeEventListener('mousedown', handleClickOutside)
    }
  }, [isOpen, onClose])

  const filteredEmojis = searchQuery
    ? Object.values(EMOJI_CATEGORIES)
        .flatMap(cat => cat.emojis)
        .filter(emoji => emoji.includes(searchQuery))
    : EMOJI_CATEGORIES[activeCategory]?.emojis || []

  if (!isOpen) return null

  return (
    <div
      ref={pickerRef}
      style={{
        position: 'absolute',
        bottom: '100%',
        left: 0,
        marginBottom: '8px',
        background: 'var(--surface-color)',
        border: '1px solid var(--glass-border)',
        borderRadius: '16px',
        boxShadow: '0 8px 32px rgba(0,0,0,0.4)',
        width: '320px',
        maxHeight: '400px',
        display: 'flex',
        flexDirection: 'column',
        zIndex: 1000,
        overflow: 'hidden',
        backdropFilter: 'blur(20px)',
        animation: 'slideUp 0.2s ease-out'
      }}
    >
      <style jsx>{`
        @keyframes slideUp {
          from {
            opacity: 0;
            transform: translateY(10px);
          }
          to {
            opacity: 1;
            transform: translateY(0);
          }
        }
      `}</style>

      {/* Search Bar */}
      <div style={{ padding: '12px', borderBottom: '1px solid var(--glass-border)' }}>
        <input
          type="text"
          placeholder="Search emojis..."
          value={searchQuery}
          onChange={(e) => {
            setSearchQuery(e.target.value)
            if (e.target.value) setActiveCategory(null)
          }}
          style={{
            width: '100%',
            padding: '8px 12px',
            background: 'rgba(255,255,255,0.05)',
            border: '1px solid var(--glass-border)',
            borderRadius: '8px',
            color: 'white',
            fontSize: '14px',
            outline: 'none'
          }}
        />
      </div>

      {/* Category Tabs */}
      {!searchQuery && (
        <div style={{
          display: 'flex',
          gap: '4px',
          padding: '8px',
          borderBottom: '1px solid var(--glass-border)',
          overflowX: 'auto',
          background: 'rgba(255,255,255,0.02)'
        }}>
          {Object.keys(EMOJI_CATEGORIES).map(category => (
            <button
              key={category}
              onClick={() => setActiveCategory(category)}
              style={{
                padding: '6px 10px',
                background: activeCategory === category ? 'var(--accent-purple)' : 'transparent',
                border: 'none',
                borderRadius: '8px',
                color: activeCategory === category ? 'white' : 'var(--text-secondary)',
                cursor: 'pointer',
                display: 'flex',
                alignItems: 'center',
                gap: '4px',
                fontSize: '12px',
                fontWeight: 600,
                whiteSpace: 'nowrap',
                transition: 'all 0.2s'
              }}
              title={category}
            >
              {EMOJI_CATEGORIES[category].icon}
            </button>
          ))}
        </div>
      )}

      {/* Emoji Grid */}
      <div style={{
        padding: '12px',
        overflowY: 'auto',
        maxHeight: '280px',
        display: 'grid',
        gridTemplateColumns: 'repeat(8, 1fr)',
        gap: '4px'
      }}>
        {filteredEmojis.map((emoji, index) => (
          <button
            key={`${emoji}-${index}`}
            onClick={() => {
              onSelect(emoji)
              onClose()
            }}
            style={{
              background: 'transparent',
              border: 'none',
              borderRadius: '6px',
              fontSize: '24px',
              cursor: 'pointer',
              padding: '6px',
              transition: 'all 0.15s',
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'center'
            }}
            onMouseEnter={(e) => {
              e.currentTarget.style.background = 'rgba(139, 92, 246, 0.2)'
              e.currentTarget.style.transform = 'scale(1.2)'
            }}
            onMouseLeave={(e) => {
              e.currentTarget.style.background = 'transparent'
              e.currentTarget.style.transform = 'scale(1)'
            }}
          >
            {emoji}
          </button>
        ))}
      </div>
    </div>
  )
}
